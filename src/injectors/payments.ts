// ─── Payments Injector ────────────────────────────────────────────────────────
// Generates Stripe helpers and API route files based on the pricing model,
// billing interval, and onboarding flow.
//
//   flat       → single flat-rate subscription (monthly or annual price IDs)
//   per-seat   → per-unit subscription line items (monthly or annual)
//   usage      → metered checkout (aggregate_usage: sum price)
//   hybrid     → flat base subscription + usage overage item on the same subscription
//   one-time   → payment_intent mode checkout (not subscription)
//   credits    → one-time payment for a credit pack + DB ledger helper
//
//   onboardingFlow = trial-* → adds trial_period_days to checkout session
//   billingInterval = annual → injects STRIPE_ANNUAL_PRICE_ID env + billing toggle helper
import type { FileEntry, PricingModel, BillingInterval, OnboardingFlow } from '../types';

// ── Trial helper ──────────────────────────────────────────────────────────────

function trialBlock(onboardingFlow: OnboardingFlow): string {
  if (onboardingFlow === 'trial-card' || onboardingFlow === 'trial-no-card') {
    return `    trial_period_days: parseInt(process.env.STRIPE_FREE_TRIAL_DAYS ?? '14'),`;
  }
  return '';
}

// ── Stripe helper templates ───────────────────────────────────────────────────

function flatStripe(billingInterval: BillingInterval, onboardingFlow: OnboardingFlow): string {
  const priceEnvKey = billingInterval === 'annual'
    ? '// Pass priceId as STRIPE_MONTHLY_PRICE_ID or STRIPE_ANNUAL_PRICE_ID depending on user choice'
    : '// Pass priceId as STRIPE_MONTHLY_PRICE_ID';
  const trial = trialBlock(onboardingFlow);
  return `// prodify-layer/payments/stripe.ts
// Generated by Prodify — flat subscription Stripe billing helpers
import Stripe from 'stripe';

export const stripe = new Stripe(process.env.STRIPE_SECRET_KEY!, {
  apiVersion: '2024-04-10',
});

${priceEnvKey}
export async function createFlatSubscription(params: {
  customerId: string;
  priceId: string;
  successUrl: string;
  cancelUrl: string;
}): Promise<Stripe.Checkout.Session> {
  return stripe.checkout.sessions.create({
    customer: params.customerId,
    mode: 'subscription',
    line_items: [{ price: params.priceId, quantity: 1 }],
    ${trial}
    success_url: params.successUrl,
    cancel_url: params.cancelUrl,
  });
}
`;
}

