const webhookRoute = `// prodify-layer/routes/api/webhooks/stripe/route.ts
// Drop into app/api/webhooks/stripe/route.ts
//
// ⚠️  TRUST NOTE: Prodify never sees your Stripe secret key or your customers'
// payment data. This webhook handler runs inside YOUR deployed app and hits
// YOUR Stripe account. Prodify injected the code — you own the data.
import { NextResponse } from 'next/server';
import { stripe } from '@/prodify-layer/payments/stripe';
import { insforge } from '@/lib/insforge';
import Stripe from 'stripe';

export async function POST(req: Request) {
  const body = await req.text();
  const sig = req.headers.get('stripe-signature');
  if (!sig) {
    return NextResponse.json({ error: 'Missing stripe-signature header' }, { status: 400 });
  }

  let event: Stripe.Event;
  try {
    event = stripe.webhooks.constructEvent(
      body,
      sig,
      process.env.STRIPE_WEBHOOK_SECRET!,
    );
  } catch (err) {
    return NextResponse.json({ error: 'Webhook signature failed' }, { status: 400 });
  }

  // Persist raw event for idempotency / replay
  await insforge.database
    .from('webhook_events')
    .upsert(
      { stripe_event_id: event.id, type: event.type, payload: JSON.stringify(event) },
      { onConflict: 'stripe_event_id' },
    );

  switch (event.type) {
    case 'checkout.session.completed': {
      const session = event.data.object as Stripe.Checkout.Session;

      if (session.metadata?.type === 'credit-purchase') {
        // Add purchased credits to user's DB balance
        const credits = parseInt(session.metadata.credits ?? '0');
        // TODO: look up user by customer ID and increment credits_remaining
        console.log('Credit purchase complete, add', credits, 'credits for customer:', session.customer);
        break;
      }

      // For subscriptions: provision access in DB
      console.log('Subscription checkout complete:', session.id);
      // TODO: update subscription status to 'active' in DB
      break;
    }
    case 'customer.subscription.updated': {
      const sub = event.data.object as Stripe.Subscription;
      // TODO: sync subscription status and period_end to DB
      console.log('Subscription updated:', sub.id, sub.status);
      break;
    }
    case 'customer.subscription.deleted': {
      const sub = event.data.object as Stripe.Subscription;
      // TODO: revoke access in DB (set status to 'cancelled')
      console.log('Subscription cancelled:', sub.id);
      break;
    }
    case 'invoice.payment_failed': {
      const invoice = event.data.object as Stripe.Invoice;
      // TODO: send payment failed email, set subscription to 'past_due' in DB
      console.log('Payment failed for customer:', invoice.customer);
      break;
    }
  }

  return NextResponse.json({ received: true });
}
`;

