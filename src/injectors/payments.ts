// ── Public API ────────────────────────────────────────────────────────────────

export function buildPaymentsFiles(
  pricingModel: PricingModel,
  billingInterval: BillingInterval,
  onboardingFlow: OnboardingFlow,
): FileEntry[] {
  const stripeContent =
    pricingModel === 'credits'  ? creditsStripe() :
    pricingModel === 'one-time' ? oneTimeStripe() :
    pricingModel === 'hybrid'   ? hybridStripe(billingInterval, onboardingFlow) :
    pricingModel === 'usage'    ? usageStripe(onboardingFlow) :
    pricingModel === 'per-seat' ? perSeatStripe(billingInterval, onboardingFlow) :
                                  flatStripe(billingInterval, onboardingFlow);

  return [
    { relativePath: 'prodify-layer/payments/stripe.ts', content: stripeContent },
    { relativePath: 'prodify-layer/routes/api/checkout/route.ts', content: buildCheckoutRoute(pricingModel, onboardingFlow) },
    { relativePath: 'prodify-layer/routes/api/webhooks/stripe/route.ts', content: webhookRoute },
    { relativePath: 'prodify-layer/routes/api/portal/route.ts', content: portalRoute },
  ];
}
