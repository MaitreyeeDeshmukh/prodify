function creditsStripe(): string {
  return `// prodify-layer/payments/stripe.ts
// Generated by Prodify — credits / token pool Stripe helpers
//
// Architecture: Stripe is the PAYMENT layer only. Your InsForge/Supabase DB
// is the LEDGER. Flow:
//   1. User buys a credit pack via Stripe Checkout (one-time payment).
//   2. Webhook fires checkout.session.completed → you add credits to their DB row.
//   3. Each AI call or action: decrement credits_remaining in DB.
//   4. Gate the API route when credits_remaining <= 0.
//
// Benefits over Stripe metered: live balance in UI, partial refunds, gifting credits,
// trial credits, no Stripe API call needed to check balance.
import Stripe from 'stripe';

export const stripe = new Stripe(process.env.STRIPE_SECRET_KEY!, {
  apiVersion: '2024-04-10',
});

/** Available credit packs — map to price IDs you create in Stripe dashboard */
export const CREDIT_PACKS = {
  starter:  { priceId: process.env.STRIPE_CREDITS_STARTER_PRICE_ID!,  credits: 100,  label: '100 credits' },
  growth:   { priceId: process.env.STRIPE_CREDITS_GROWTH_PRICE_ID!,   credits: 500,  label: '500 credits' },
  scale:    { priceId: process.env.STRIPE_CREDITS_SCALE_PRICE_ID!,    credits: 2000, label: '2,000 credits' },
} as const;

export type CreditPack = keyof typeof CREDIT_PACKS;

/** Create a Stripe Checkout session for purchasing a credit pack */
export async function createCreditPackCheckout(params: {
  customerId: string;
  pack: CreditPack;
  successUrl: string;
  cancelUrl: string;
}): Promise<Stripe.Checkout.Session> {
  const { priceId, credits } = CREDIT_PACKS[params.pack];
  return stripe.checkout.sessions.create({
    customer: params.customerId,
    mode: 'payment',
    line_items: [{ price: priceId, quantity: 1 }],
    success_url: params.successUrl,
    cancel_url: params.cancelUrl,
    payment_intent_data: {
      metadata: {
        type: 'credit-purchase',
        pack: params.pack,
        credits: String(credits),
      },
    },
  });
}
`;
}

