function signInButtonShadcn(authMethods: AuthMethod[]): string {
  const buttons = authMethods
    .filter((m) => ['google', 'github', 'magic-link', 'email-pass'].includes(m))
    .map((m) => {
      if (m === 'google') return `      <Button variant="outline" className="w-full" onClick={() => signIn('google', { callbackUrl: '/' })}>Sign in with Google</Button>`;
      if (m === 'github') return `      <Button className="w-full bg-[#24292e] text-white" onClick={() => signIn('github', { callbackUrl: '/' })}>Sign in with GitHub</Button>`;
      if (m === 'magic-link') return `      <MagicLinkForm />`;
      if (m === 'email-pass') return `      <EmailPasswordForm />`;
      return '';
    })
    .filter(Boolean)
    .join('\n');

  return `// prodify-layer/ui/auth/sign-in-button.tsx
// Generated by Prodify — mount this wherever you need login buttons.
// shadcn/ui variant — requires @radix-ui/react-slot and class-variance-authority.
'use client';
import { signIn } from 'next-auth/react';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';

${authMethods.includes('magic-link') ? `function MagicLinkForm() {
  const handleSubmit = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    const form = e.currentTarget;
    const email = (form.elements.namedItem('email') as HTMLInputElement).value;
    await signIn('email', { email, callbackUrl: '/' });
  };
  return (
    <form onSubmit={handleSubmit} className="flex flex-col gap-3">
      <Input name="email" type="email" placeholder="your@email.com" />
      <Button type="submit" className="w-full">Send magic link</Button>
    </form>
  );
}` : ''}

export function SignInButton() {
  return (
    <div className="flex flex-col gap-3">
${buttons}
    </div>
  );
}
`;
}

