function billingPortalButtonPlain(): string {
  return `// prodify-layer/ui/pricing/billing-portal-button.tsx
// Generated by Prodify
// Mount in your dashboard wherever the user manages their subscription.
'use client';
import { useState } from 'react';

export function BillingPortalButton() {
  const [loading, setLoading] = useState(false);

  const handleClick = async () => {
    setLoading(true);
    try {
      const res = await fetch('/api/portal', { method: 'POST' });
      const data = await res.json() as { url?: string; error?: string };
      if (data.url) {
        window.location.href = data.url;
      } else {
        console.error('Portal error:', data.error);
      }
    } finally {
      setLoading(false);
    }
  };

  return (
    <button
      onClick={handleClick}
      disabled={loading}
      style={{ padding: '10px 20px', background: '#111', color: '#fff',
               border: 'none', borderRadius: 8, cursor: loading ? 'not-allowed' : 'pointer',
               fontSize: 14, opacity: loading ? 0.7 : 1 }}
    >
      {loading ? 'Loading…' : 'Manage subscription'}
    </button>
  );
}
`;
}

