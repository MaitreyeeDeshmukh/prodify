// ── Freemium Usage Meter ──────────────────────────────────────────────────────

function usageMeterPlain(): string {
  return `// prodify-layer/ui/freemium/usage-meter.tsx
// Generated by Prodify — freemium onboarding model
// Shows how much of the free allowance the user has consumed.
'use client';
import { useEffect, useState } from 'react';

interface UsageData {
  used: number;
  limit: number;
  label: string;   // e.g. "API calls this month"
}

export function UsageMeter() {
  const [usage, setUsage] = useState<UsageData | null>(null);

  useEffect(() => {
    fetch('/api/usage')
      .then((r) => r.json())
      .then((d: UsageData) => setUsage(d))
      .catch(console.error);
  }, []);

  if (!usage) return null;

  const pct = Math.min((usage.used / usage.limit) * 100, 100);
  const isNearLimit = pct >= 80;

  return (
    <div style={{ maxWidth: 320 }}>
      <div style={{ display: 'flex', justifyContent: 'space-between', marginBottom: 6, fontSize: 13 }}>
        <span style={{ color: '#666' }}>{usage.label}</span>
        <span style={{ fontWeight: 600, color: isNearLimit ? '#dc2626' : '#111' }}>
          {usage.used.toLocaleString()} / {usage.limit.toLocaleString()}
        </span>
      </div>
      <div style={{ height: 6, background: '#e5e7eb', borderRadius: 99 }}>
        <div
          style={{
            height: '100%',
            width: \`\${pct}%\`,
            background: isNearLimit ? '#dc2626' : '#2563eb',
            borderRadius: 99,
            transition: 'width 0.3s ease',
          }}
        />
      </div>
      {isNearLimit && (
        <p style={{ marginTop: 8, fontSize: 12, color: '#dc2626' }}>
          You're close to your free limit.{' '}
          <a href="/pricing" style={{ textDecoration: 'underline' }}>Upgrade to Pro →</a>
        </p>
      )}
    </div>
  );
}
`;
}

// ── Main builder ──────────────────────────────────────────────────────────────

export function buildUiFiles(answers: ProdifyAnswers): FileEntry[] {
  if (!answers.injectUi) return [];

  const lib: UILibrary = answers.uiLibrary ?? 'plain';
  const files: FileEntry[] = [];

  // Auth components
  files.push({
    relativePath: 'prodify-layer/ui/auth/sign-in-button.tsx',
    content: lib === 'shadcn'
      ? signInButtonShadcn(answers.authMethods)
      : signInButtonPlain(answers.authMethods),
  });

  files.push({
    relativePath: 'prodify-layer/ui/auth/sign-out-button.tsx',
    content: lib === 'shadcn' ? signOutButtonShadcn() : signOutButtonPlain(),
  });

  // Pricing components
  files.push({
    relativePath: 'prodify-layer/ui/pricing/billing-portal-button.tsx',
    content: lib === 'shadcn' ? billingPortalButtonShadcn() : billingPortalButtonPlain(),
  });

  files.push({
    relativePath: 'prodify-layer/ui/pricing/pricing-page.tsx',
    content: pricingPagePlain(answers.pricingModel), // same for both libs for now
  });

  // Credits-specific
  if (answers.pricingModel === 'credits') {
    files.push({
      relativePath: 'prodify-layer/ui/credits/credit-balance.tsx',
      content: creditBalancePlain(),
    });
  }

  // Freemium-specific
  if (answers.onboardingFlow === 'freemium') {
    files.push({
      relativePath: 'prodify-layer/ui/freemium/usage-meter.tsx',
      content: usageMeterPlain(),
    });
  }

  return files;
}
